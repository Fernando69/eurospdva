<h1>La institución</h1>

<h2>Misión</h2>
<p>El colegio es Católico-Salesiano que busca la formación integral de los jóvenes. A través de la pedagogía centrada en tres ejes transversales: razón, religión y amor, llamado el “Sistema Preventivo”. Su finalidad es que los jóvenes traduzcan su credo en obras concretas de bien común y en esperanza de transformar la realidad social con la óptica del Evangelio.</p>
<p>Joven y adolescente es el centro del hecho educativo ayudándolo a crecer en una amplia gama de valores Humanos-Cristianos, a cultivar los valores patrios y a fortalecer la identidad nacional.</p>

<h2>Visión</h2>
<p>El colegio Don Bosco quiere formar jóvenes para la vida, consciente de su dignidad, capaces de liberarse a sí mismo en unión con los demás, comprendidos con los valores recibidos de Dios, con sensibilidad social y política, solidarios, con vocación de servicio por su comunidad, con conciencia critica. Jóvenes que descubran y opten por Cristo como liberador y amigo, comprometidos con la formación y crecimiento integral de su propia familia, con un educador dotado de coherencia personal que sea para los jóvenes punto de referencia educativa, que posea madurez humana, competencia educativa e identificado con el ideal Salesiano</p>

<h2>Objetivo General</h2>
<p>La comunidad del colegio Don Bosco de Valencia se propone una acción educativa evangelizadora dirigida preferentemente a los niños y adolescentes más necesitados, que privilegie la formación Humano-Cristiana, la educación para el trabajo, la interacción corresponsable de todos los miembros en un ambiente según el estilo y método de Don Bosco en estrecha vinculación con la Iglesia local y el entorno social.</p>	

<h2>Objetivos Específicos</h2>
<ul>
	<li>Mantener la identidad del Colegio “Don Bosco” como centro educativo católico y Salesiano.</li>
	<li>Recibir en el colegio a los niños y jóvenes más necesitados acorde con las opciones de los Salesianos y del proyecto AVEC.</li>
	<li>Reforzar nuestra oferta de educación para el trabajo.</li>
	<li>Lograr que nuestra comunidad educativa pastoral amplíe su radio de relación y de acción con proyección en diferentes ambientes.</li>
	<li>Lograr la vivencia de las dimensiones fundamentales del Sistema Preventivo.</li>
</ul>

